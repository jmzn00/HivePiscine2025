int	stoa(char str[], int array[])
{
	int	i;
	int	o;

	i = 0;
	o = 0;
	while (str [i] != '\0')
	{
		if (str [i] == ' ')
		{
			i++;
		}
		if (str [i] >= '0' && str [i] <= '9')
		{
			array [o] = str [i] - 48;
			o++;
		}
		i++;
	}
	return (0);
}
